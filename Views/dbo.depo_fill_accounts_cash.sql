CREATE VIEW [dbo].[depo_fill_accounts_cash]
AS
	SELECT A.*
	FROM dbo.ACCOUNTS A
	WHERE (A.REC_STATE NOT IN (2, 128)) AND (A.ACC_TYPE = 2) AND
		(A.BAL_ACC_ALT BETWEEN 1003.00 AND 1003.99 OR
		 A.BAL_ACC_ALT BETWEEN 1005.00 AND 1005.99 OR
		 A.BAL_ACC_ALT BETWEEN 1013.00 AND 1013.99 OR
		 A.BAL_ACC_ALT BETWEEN 1015.00 AND 1015.99)
GO
