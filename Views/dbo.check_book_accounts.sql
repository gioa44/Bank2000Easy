CREATE VIEW [dbo].[check_book_accounts] AS

SELECT A.*
FROM dbo.ACCOUNTS A
WHERE A.CLIENT_NO IS NOT NULL AND NOT REC_STATE IN (2,128) AND
		(A.BAL_ACC_ALT BETWEEN 3301 AND 3306.99 OR
		A.BAL_ACC_ALT BETWEEN 3311 AND 3316.99 OR
		A.BAL_ACC_ALT BETWEEN 3402 AND 3408.99 OR
		A.BAL_ACC_ALT BETWEEN 3412 AND 3418.99 OR
		A.BAL_ACC_ALT BETWEEN 3602 AND 3608.99 OR
		A.BAL_ACC_ALT BETWEEN 3612 AND 3618.99)
GO
