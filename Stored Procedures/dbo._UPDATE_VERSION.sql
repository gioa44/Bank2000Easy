CREATE PROCEDURE [dbo].[_UPDATE_VERSION] 
  @ver_name varchar(20)
AS

SET NOCOUNT ON
IF EXISTS(SELECT * FROM	INI_INT WHERE IDS = @ver_name)
BEGIN
  UPDATE INI_INT
  SET VALS = VALS + 1
  WHERE IDS = @ver_name
END
ELSE
  INSERT INTO INI_INT (IDS,VALS) VALUES (@ver_name,1)



GO
